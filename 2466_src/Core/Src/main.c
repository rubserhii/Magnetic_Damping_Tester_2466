/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  uint32_t current_tick;
  // First update should be right away
  uint32_t last_blink_tick = -LED_BLINK_INTERVAL;
  uint32_t last_update_tick = -500; // TODO: check this
  uint32_t led_cycle = 0;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_USART1_UART_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */

  // INITIALIZATION 
  
  DebugIO_Init(&huart2); // printf USART2
  
  HAL_TIM_Base_Start(&htim1);
  HAL_TIM_Encoder_Start(&htim2, TIM_CHANNEL_ALL);
  // TODO: start TIM3 PWM https://controllerstech.com/pwm-in-stm32/

  if(HAL_ADCEx_Calibration_Start(&hadc1) != HAL_OK){
    Error_Handler();
  }
  HAL_ADC_Start(&hadc1);

  CONTROL_initIMU(&huart1); // IMU USART1

  FSM_init();

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

    current_tick = HAL_GetTick();
    
    switch (state){

      case INIT:
        FSM_init();
        break;
      
      case PRE_DUT:
        FSM_pre_dut();
        break;
      
      case DUT:
        FSM_dut();  
        break;
      
      case POST_DUT:
        FSM_post_dut();
        break;
      
      case DEINIT:
        FSM_deinit();  
        break;

    }

    // accel happens in USART2 interrupt at speed of IMU
    loadcell = CONTROL_readLoadCell(&hadc1);
    encoder_count = CONTROL_readEncoder(TIM2);

    // blink LED
    if (current_tick - last_blink_tick >= LED_BLINK_INTERVAL)
    {
      uint32_t led_state = 0;
      led_state = (LED_NORMAL_BLINK_PATTERN >> led_cycle) & 0x1U;
      led_cycle = (led_cycle + 1U) % LED_BLINK_PATTERN_LENGTH;
      HAL_GPIO_WritePin(LED_OUT_GPIO_Port, LED_OUT_Pin, led_state);
      last_blink_tick = current_tick;
    }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

